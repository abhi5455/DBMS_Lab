    DECLARE
        INCREMENT NUMBER(10,2);
    BEGIN
        INCREMENT := :NEW.SALARY - :OLD.SALARY;
        IF INCREMENT > 1000 THEN 
            INSERT INTO INCR(EMPID, INCR) VALUES(:NEW.EMPID, INCREMENT);
        END IF;
    END;
END;
